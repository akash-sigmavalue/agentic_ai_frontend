export const DEFAULT_GOOGLE_FORMS_ACTION_KEY: GoogleFormsActionKey = 'enable-form-trigger';

export function getGoogleFormsActionDefinition(actionKey: GoogleFormsActionKey): GoogleFormsActionDefinition {
  return GOOGLE_FORMS_ACTION_DEFINITIONS.find((definition) => definition.key === actionKey) || GOOGLE_FORMS_ACTION_DEFINITIONS[0];
}

export function buildInitialGoogleFormsFormValues(action: GoogleFormsActionDefinition): GoogleFormsFormValues {
  const values: GoogleFormsFormValues = {};
  action.fields.forEach((field) => {
    if (field.type === 'select' && field.required) {
      values[field.key] = field.options?.[0]?.value || '';
      return;
    }
    values[field.key] = '';
  });
  return values;
}
