'use client';

export type GoogleFormsActionKey = 'enable-form-trigger' | 'submission-router';
export type GoogleFormsActionBadge = 'Trigger' | 'Workflow';
export type GoogleFormsFieldType = 'text' | 'textarea' | 'select';

export interface GoogleFormsFieldOption {
  label: string;
  value: string;
}

export interface GoogleFormsFormValues {
  [key: string]: string;
}

export interface GoogleFormsFieldDefinition {
  key: string;
  label: string;
  type: GoogleFormsFieldType;
  required: boolean;
  placeholder: string;
  helperText?: string;
  options?: GoogleFormsFieldOption[];
  spanFull?: boolean;
}

export interface GoogleFormsActionDefinition {
  key: GoogleFormsActionKey;
  badge: GoogleFormsActionBadge;
  title: string;
  description: string;
  frontendRule: string;
  steps: string[];
  fields: GoogleFormsFieldDefinition[];
}

export interface GoogleFormsBrowserItem {
  key: GoogleFormsActionKey;
  section: 'Triggers' | 'Workflow';
  title: string;
  description: string;
  badge: GoogleFormsActionBadge;
}

