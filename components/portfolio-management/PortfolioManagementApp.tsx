const normalizeColumnName = (value) =>
  String(value || '')
    .toLowerCase()
    .replace(/[^a-z0-9]/g, '');

const findBestColumn = (fieldName, columns) => {
  const normalizedField = normalizeColumnName(fieldName);
  if (!normalizedField) return '';

  const exact = columns.find((column) => normalizeColumnName(column) === normalizedField);
  if (exact) return exact;

  const contains = columns.find((column) => {
    const normalizedColumn = normalizeColumnName(column);
    return normalizedColumn.includes(normalizedField) || normalizedField.includes(normalizedColumn);
  });
  if (contains) return contains;

  const fieldTokens = String(fieldName || '').toLowerCase().split(/[^a-z0-9₹]+/).filter((token) => token.length > 2);
  let bestColumn = '';
  let bestScore = 0;

  columns.forEach((column) => {
    const normalizedColumnLabel = String(column || '').toLowerCase();
    const score = fieldTokens.reduce((sum, token) => sum + (normalizedColumnLabel.includes(token) ? 1 : 0), 0);
    if (score > bestScore) {
      bestScore = score;
      bestColumn = column;
    }
  });

  return bestScore > 0 ? bestColumn : '';
};

