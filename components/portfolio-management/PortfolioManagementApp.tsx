function makeId(prefix = 'id') {
  return `${prefix}-${Date.now()}-${Math.random().toString(16).slice(2)}`;
}

function defaultValueForField(field, index) {
  if (sampleValues[field.name]) return sampleValues[field.name][index] || '';
  if (field.type === 'system') return `${field.name.replace(/[^a-z0-9]/gi, '').toUpperCase()}-${Math.floor(Math.random() * 90000 + 10000)}`;
  return '';
}

function makeSections() {
  return sourceSections.map((section, sectionIndex) => {
    const fields = section.fields.map(([name, type], fieldIndex) => ({
      id: `section-${sectionIndex}-field-${fieldIndex}`,
      name,
      type,
      description: derivedFieldInfo[name]?.formula || fieldTypeMeta[type].note
    }));
    const records = (starterRecords[sectionIndex] || [0, 1].map((recordIndex) => Object.fromEntries(fields.map((field) => [field.name, defaultValueForField(field, recordIndex)]))));
    return {
      id: `section-${sectionIndex}`,
      name: section.name,
      masterName: section.masterName,
      fields,
      records
    };
  });
}



