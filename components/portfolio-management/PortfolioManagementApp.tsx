const toUiRecord = (backendRecord, fields) => {
  const recordData = backendRecord?.record_data || {};
  const customFields = backendRecord?.custom_fields || {};
  const derivedAudit = backendRecord?.derived_audit || {};
  const record = {
    __backendId: backendRecord?.id,
    __assetId: backendRecord?.asset_id,
    __validationStatus: backendRecord?.validation_status,
    __validationErrors: backendRecord?.validation_errors || [],
    __derivedAudit: derivedAudit,
    __customFields: customFields
  };
  fields.forEach((field) => {
    const audit = field.backendKey ? derivedAudit.fields?.[field.backendKey] : null;
    const auditValue = auditDisplayValue(audit);
    record[field.name] = hasAuditValue(auditValue)
      ? auditValue
      : recordData[field.backendKey] ?? '';
  });
  Object.entries(customFields).forEach(([key, value]) => {
    if (record[key] === undefined) record[key] = value;
  });
  return record;
};

