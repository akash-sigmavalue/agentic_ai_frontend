const parseDateValue = (value) => {
  if (!value) return null;
  const parsed = new Date(value);
  return Number.isNaN(parsed.getTime()) ? null : parsed;
};

const yearsBetweenDates = (startDateValue, endDateValue) => {
  const startDate = parseDateValue(startDateValue);
  const endDate = parseDateValue(endDateValue);
  if (!startDate || !endDate) return 0;
  const diffDays = (endDate - startDate) / (1000 * 60 * 60 * 24);
  return diffDays > 0 ? diffDays / 365 : 0;
};


const pmt = (annualRate, months, principal) => {
  const r = toNumber(annualRate) / 100 / 12;
  const p = toNumber(principal);
  if (!p || !months) return '';
  if (!r) return p / months;
  return (p * r) / (1 - Math.pow(1 + r, -months));
};


